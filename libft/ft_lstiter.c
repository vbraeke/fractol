#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*itm;

	if (lst && f)
	{
		itm = lst->next;
		f(lst);
		ft_lstiter(itm, f);
	}
}
